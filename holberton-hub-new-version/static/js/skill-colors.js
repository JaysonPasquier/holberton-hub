/**
 * Apply colors to all skill badges on the page
 */
function applySkillColors() {
    document.querySelectorAll('.skill-badge').forEach(badge => {
        const skillCode = badge.dataset.skillCode;

        // Apply color based on skill code
        if (skillCode && window.skillColors[skillCode]) {
            badge.style.backgroundColor = window.skillColors[skillCode];
            badge.style.color = 'white';
        } else {
            // Default color if not found
            badge.style.backgroundColor = '#6c757d';
            badge.style.color = 'white';
        }
    });
}

/**
 * Initialize skill colors when the DOM is ready
 */
document.addEventListener('DOMContentLoaded', function() {
    applySkillColors();

    // Also apply colors when dynamic content is loaded
    document.addEventListener('contentLoaded', applySkillColors);

    // If using AJAX navigation, re-apply when needed
    if (typeof window.addEventListener === 'function') {
        window.addEventListener('DOMContentLoaded', applySkillColors);
        window.addEventListener('load', applySkillColors);
    }
});
